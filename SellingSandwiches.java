import java.util.Scanner;
// Selling Sandwiches 
// https://www.codechef.com/START195D/problems/SANDWSHOP

public class SellingSandwiches {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        
        int A = sc.nextInt();
        
        int B = sc.nextInt();
        
        int C = sc.nextInt();
        
        
        System.out.println(A - (B + C));

    }
}
